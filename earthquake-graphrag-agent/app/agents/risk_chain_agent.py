from __future__ import annotations

from typing import Dict, List


class RiskChainAgent:
    """风险链构建 Agent：将灾情关键词组织为当前风险因果链。"""

    RULES = [
        ("房屋倒塌", "建筑物倒塌导致人员被困和伤亡风险上升"),
        ("建筑倒塌", "建筑物倒塌导致人员被困和伤亡风险上升"),
        ("建筑物受损", "建筑结构受损导致次生坍塌风险上升"),
        ("人员被困", "人员被困导致生命搜救需求紧迫"),
        ("人员伤亡", "人员伤亡导致医疗救治和转运压力上升"),
        ("伤员", "伤员集中导致医疗分诊和转运压力上升"),
        ("道路塌方", "道路塌方导致救援通道受阻"),
        ("交通中断", "交通中断导致救援力量和物资进入困难"),
        ("通信中断", "通信中断导致灾情信息不完整和指挥协同困难"),
        ("电力中断", "电力中断影响通信、医疗和安置保障"),
        ("余震", "余震可能引发二次坍塌和救援人员安全风险"),
        ("滑坡", "强震诱发滑坡并威胁道路和居民点安全"),
        ("崩塌", "山体崩塌导致道路阻断和人员伤害风险"),
        ("落石", "落石风险威胁救援通道和现场人员安全"),
        ("堰塞湖", "堰塞湖水位上涨导致下游溃决洪水风险"),
        ("洪水", "洪水威胁下游居民点和救援路线安全"),
        ("危化品", "危化品设施受损导致泄漏和中毒风险"),
        ("泄漏", "泄漏扩散导致周边居民暴露风险上升"),
        ("火灾", "火灾扩大导致人员疏散和消防处置压力上升"),
        ("爆炸", "爆炸风险要求封控危险区并组织专业处置"),
        ("学校", "学校人员密集导致疏散踩踏和秩序维护风险"),
        ("学生", "学生群体需要集中清点、安抚和安全转移"),
        ("医院", "医院接诊压力上升导致医疗资源调配需求增加"),
        ("医疗", "医疗资源紧张导致分诊转运需求增加"),
        ("低温", "低温环境导致临时安置人员失温风险上升"),
        ("寒冷", "寒冷环境导致保暖物资和临时安置需求增加"),
        ("失温", "失温风险要求优先保障帐篷、棉被和取暖设备"),
        ("安置", "群众转移安置导致避难场所和物资保障压力上升"),
        ("避难", "避难需求集中导致安置容量和秩序维护压力上升"),
        ("物资", "物资缺口影响受灾群众基本生活保障"),
        ("帐篷", "帐篷需求增加反映临时安置压力上升"),
        ("老人", "老人等脆弱人群健康风险较高"),
        ("儿童", "儿童等脆弱人群需要重点保护和心理安抚"),
    ]

    def run(self, extracted_event: Dict) -> List[str]:
        text = extracted_event.get("raw_text", "")
        magnitude = extracted_event.get("magnitude")
        chain: List[str] = []

        if magnitude is not None:
            if magnitude >= 7.0:
                chain.append("强震造成大范围建筑、交通和通信系统受损")
            elif magnitude >= 6.0:
                chain.append("较强地震造成局部建筑破坏和应急资源需求上升")
            else:
                chain.append("地震造成局部震感和重点区域安全排查需求")
        else:
            chain.append("地震灾情信息不完整，需要快速核实震级、震中和影响范围")

        for keyword, risk in self.RULES:
            if keyword in text and risk not in chain:
                chain.append(risk)

        if len(chain) == 1:
            chain.extend([
                "灾情细节不足导致风险判断存在不确定性",
                "需要通过现场核查、遥感影像、群众报送和部门数据补充灾情信息",
            ])

        return chain
